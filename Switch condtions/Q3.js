function printGrade(grade){
    switch(grade){
        case 'A':
            console.log("Excellent");
            break;
        case B:
            console.log("Very Good");
            break;
        case C:
            console.log("Good");
            break;
        case D:
            console.log("Average");
        case E:
            console.log("Satisfactory");
            break;
        case F:
            console.log("Fail");

    }
}
printGrade('A');